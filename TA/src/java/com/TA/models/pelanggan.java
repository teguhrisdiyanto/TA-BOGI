public class pelanggan {
    private int id_pelanggan;
    private String pelanggan_nama;
    private String  pelanggan_alamat;
    private String  pelanggan_nohp;
    private String  pelanggan_email;
    private String   pelanggan_pekerjaan;

    public int getId_pelanggan() {
        return id_pelanggan;
    }

    public void setId_pelanggan(int id_pelanggan) {
        this.id_pelanggan = id_pelanggan;
    }

    public String getPelanggan_nama() {
        return pelanggan_nama;
    }

    public void setPelanggan_nama(String pelanggan_nama) {
        this.pelanggan_nama = pelanggan_nama;
    }

    public String getPelanggan_alamat() {
        return pelanggan_alamat;
    }

    public void setPelanggan_alamat(String pelanggan_alamat) {
        this.pelanggan_alamat = pelanggan_alamat;
    }

    public String getPelanggan_nohp() {
        return pelanggan_nohp;
    }

    public void setPelanggan_nohp(String pelanggan_nohp) {
        this.pelanggan_nohp = pelanggan_nohp;
    }

    public String getPelanggan_email() {
        return pelanggan_email;
    }

    public void setPelanggan_email(String pelanggan_email) {
        this.pelanggan_email = pelanggan_email;
    }

    public String getPelanggan_pekerjaan() {
        return pelanggan_pekerjaan;
    }

    public void setPelanggan_pekerjaan(String pelanggan_pekerjaan) {
        this.pelanggan_pekerjaan = pelanggan_pekerjaan;
    }
    
    
}
